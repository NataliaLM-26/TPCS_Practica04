    public void lista(){
        DefaultTableModel model = new DefaultTableModel();
        model.addColumn("id");
        model.addColumn("fecha");
        model.addColumn("total");
        model.addColumn("cliente");
    
        Session session = HibernateUtil.getSessionFactory().openSession();
        CriteriaBuilder builder = session.getCriteriaBuilder();
        CriteriaQuery<Venta> criteria = builder.createQuery(Venta.class);
        Root<Venta> root = criteria.from(Venta.class);
        criteria.select(root);

        List<Venta> ventas = session.createQuery(criteria).getResultList();

        for (Venta venta : ventas) {
            Object[] rowData = new Object[4];
            rowData[0] = venta.getIdVenta();
            rowData[1] = venta.getFecha();
            rowData[2] = venta.getTotal();
            rowData[3] = venta.getCliente().getIdCliente();
            model.addRow(rowData);
        }
        tblVentas.setModel(model);
        session.close();
}

