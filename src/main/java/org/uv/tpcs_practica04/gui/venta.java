            Date fecha = null;
               try {
                   fecha = (Date) sdf.parse(txtFecha.getText());
               } catch (ParseException e) {
                   e.printStackTrace();
               }

         v1.setFecha(fecha);
         
            int totalInt = Integer.parseInt(txtTotal.getText());
            BigDecimal total = new BigDecimal(totalInt);

         v1.setTotal(total);
         session.save(v1);
         transaction.commit();
         session.close();
         
         txtFecha.setText("");
         txtTotal.setText("");
